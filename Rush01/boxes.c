char	boxes(char c1, char c2, char c3, char c4)
{
	char	cc;
	char	h;

	cc = '1';
	h = c1;
	if (c2 > h)
	{
		cc++;
		h = c2;
	}
	if (c3 > h)
	{
		cc++;
		h = c3;
	}
	if (c4 > h)
	{
		cc++;
	}
	return (cc);
}
