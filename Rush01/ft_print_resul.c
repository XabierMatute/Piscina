#include<unistd.h>

void	ft_print_str(char l1[4], char l2[4], char l3[4], char l4[4])
{
	write (1, l1, 4);
	write (1, "\n", 1);
	write (1, l2, 4);
	write (1, "\n", 1);
	write (1, l3, 4);
	write (1, "\n", 1);
	write (1, l4, 4);
	write (1, "\n", 1);
}
