int	ft_zerocomp(char *strzero)
{
	int	i;

	i = 0;
	while (strzero[i] != '\0')
	{
		if (strzero[i] != '0')
		{
			return (1);
		}
		else
		{
			i++;
		}
	}
	return (0);
}
